CREATE TABLE IF NOT EXISTS QRTZ5_JOB_DETAILS (
    JOB_NAME VARCHAR(200) NOT NULL,
    JOB_GROUP VARCHAR(200) NOT NULL,
    DESCRIPTION VARCHAR(250),
    JOB_CLASS_NAME VARCHAR(250) NOT NULL,
    IS_DURABLE BOOLEAN NOT NULL,
    IS_VOLATILE BOOLEAN NOT NULL,
    IS_STATEFUL BOOLEAN NOT NULL,
    REQUESTS_RECOVERY BOOLEAN NOT NULL,
    JOB_DATA BINARY,
    PRIMARY KEY(JOB_NAME, JOB_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_JOB_LISTENERS (
    JOB_NAME VARCHAR(200) NOT NULL,
    JOB_GROUP VARCHAR(200) NOT NULL,
    JOB_LISTENER VARCHAR(200) NOT NULL,
    PRIMARY KEY(JOB_NAME, JOB_GROUP, JOB_LISTENER),
    CONSTRAINT SYS_FK_48 FOREIGN KEY(JOB_NAME, JOB_GROUP) REFERENCES QRTZ5_JOB_DETAILS(JOB_NAME, JOB_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_TRIGGERS (
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    JOB_NAME VARCHAR(200) NOT NULL,
    JOB_GROUP VARCHAR(200) NOT NULL,
    IS_VOLATILE BOOLEAN NOT NULL,
    DESCRIPTION VARCHAR(250),
    NEXT_FIRE_TIME NUMERIC(13),
    PREV_FIRE_TIME NUMERIC(13),
    PRIORITY INTEGER,
    TRIGGER_STATE VARCHAR(16) NOT NULL,
    TRIGGER_TYPE VARCHAR(8) NOT NULL,
    START_TIME NUMERIC(13) NOT NULL,
    END_TIME NUMERIC(13),
    CALENDAR_NAME VARCHAR(200),
    MISFIRE_INSTR NUMERIC(2),
    JOB_DATA BINARY,
    PRIMARY KEY(TRIGGER_NAME, TRIGGER_GROUP),
    CONSTRAINT SYS_FK_53 FOREIGN KEY(JOB_NAME, JOB_GROUP) REFERENCES QRTZ5_JOB_DETAILS(JOB_NAME, JOB_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_SIMPLE_TRIGGERS (
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    REPEAT_COUNT NUMERIC(7) NOT NULL,
    REPEAT_INTERVAL NUMERIC(12) NOT NULL,
    TIMES_TRIGGERED NUMERIC(10) NOT NULL,
    PRIMARY KEY(TRIGGER_NAME, TRIGGER_GROUP),
    CONSTRAINT SYS_FK_58 FOREIGN KEY(TRIGGER_NAME, TRIGGER_GROUP) REFERENCES QRTZ5_TRIGGERS(TRIGGER_NAME, TRIGGER_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_CRON_TRIGGERS (
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    CRON_EXPRESSION VARCHAR(120) NOT NULL,
    TIME_ZONE_ID VARCHAR(80),
    PRIMARY KEY(TRIGGER_NAME, TRIGGER_GROUP),
    CONSTRAINT SYS_FK_63 FOREIGN KEY(TRIGGER_NAME, TRIGGER_GROUP) REFERENCES QRTZ5_TRIGGERS(TRIGGER_NAME, TRIGGER_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_BLOB_TRIGGERS (
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    BLOB_DATA BINARY,
    PRIMARY KEY(TRIGGER_NAME, TRIGGER_GROUP),
    CONSTRAINT SYS_FK_68 FOREIGN KEY(TRIGGER_NAME, TRIGGER_GROUP) REFERENCES QRTZ5_TRIGGERS(TRIGGER_NAME, TRIGGER_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_TRIGGER_LISTENERS (
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    TRIGGER_LISTENER VARCHAR(200) NOT NULL,
    PRIMARY KEY(TRIGGER_NAME, TRIGGER_GROUP, TRIGGER_LISTENER),
    CONSTRAINT SYS_FK_73 FOREIGN KEY(TRIGGER_NAME, TRIGGER_GROUP) REFERENCES QRTZ5_TRIGGERS(TRIGGER_NAME, TRIGGER_GROUP)
    );

CREATE TABLE IF NOT EXISTS QRTZ5_CALENDARS (
    CALENDAR_NAME VARCHAR(200) NOT NULL PRIMARY KEY,
    CALENDAR BINARY NOT NULL
    );

CREATE TABLE IF NOT EXISTS QRTZ5_PAUSED_TRIGGER_GRPS (
    TRIGGER_GROUP VARCHAR(200) NOT NULL PRIMARY KEY
    );

CREATE TABLE IF NOT EXISTS QRTZ5_FIRED_TRIGGERS (
    ENTRY_ID VARCHAR(95) NOT NULL PRIMARY KEY,
    TRIGGER_NAME VARCHAR(200) NOT NULL,
    TRIGGER_GROUP VARCHAR(200) NOT NULL,
    IS_VOLATILE BOOLEAN NOT NULL,
    INSTANCE_NAME VARCHAR(200) NOT NULL,
    FIRED_TIME NUMERIC(13) NOT NULL,
    PRIORITY INTEGER NOT NULL,
    STATE VARCHAR(16) NOT NULL,
    JOB_NAME VARCHAR(200),
    JOB_GROUP VARCHAR(200),
    IS_STATEFUL BOOLEAN,
    REQUESTS_RECOVERY BOOLEAN
    );

CREATE TABLE IF NOT EXISTS QRTZ5_SCHEDULER_STATE (
    INSTANCE_NAME VARCHAR(200) NOT NULL PRIMARY KEY,
    LAST_CHECKIN_TIME NUMERIC(13) NOT NULL,
    CHECKIN_INTERVAL NUMERIC(13) NOT NULL
    );

